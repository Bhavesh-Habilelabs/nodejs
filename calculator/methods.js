/**
 * @param {number} a
 * @param {number} b
 * @returns {number} returns sum of a and b
 */
const add = (a, b) => a + b;

/**
 * @param {number} a
 * @param {number} b
 * @returns {number} returns difference of a and b
 */
const sub = (a, b) => a - b;

/**
 * @param {number} a
 * @param {number} b
 * @returns {number} returns product of a and b
 */
const multi = (a, b) => a * b;

/**
 * @param {number} a
 * @param {number} b
 * @returns {number} returns division of a and b
 */
const div = (a, b) => a / b;

module.exports = {
  add,
  sub,
  multi,
  div
};
